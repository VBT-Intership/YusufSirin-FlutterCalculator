extension FourOperations on double {
  String plus(double a) {
    return (this + a).toString();
  }

  String minus(double a) {
    return (this - a).toString();
  }

  String multiply(double a) {
    return (this * a).toString();
  }

  String divided(double a) {
    return (this / a).toString();
  }
}
